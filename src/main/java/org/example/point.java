package org.example;

public class point {
    public int x;
    public int y;
    public int z;

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }

    public point(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
